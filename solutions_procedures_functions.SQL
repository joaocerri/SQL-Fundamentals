DELIMITER //
CREATE FUNCTION get_salary(E_EMP_NO INT)
RETURNS INT
DETERMINISTIC 
BEGIN
    DECLARE SALARIO INT;
    SELECT S.SALARY INTO SALARIO FROM SALARIES S
    JOIN EMPLOYEES E ON E.EMP_NO = S.EMP_NO
    WHERE S.TO_DATE = '9999-01-01' AND S.EMP_NO = E_EMP_NO
    LIMIT 1;
    RETURN SALARIO;
END //
DELIMITER ;

DELIMITER //
CREATE PROCEDURE proc_compare_salary(IN EMP_ID INT, IN SALARY_THR DECIMAL(10,2))
BEGIN 
    DECLARE EMP_SALARY DECIMAL(10,2);
    SET EMP_SALARY = GET_SALARY(EMP_ID);
    
    IF EMP_SALARY > SALARY_THR THEN 
        SELECT CONCAT('O funcionário ', EMP_ID, ' ganha mais que $', SALARY_THR) AS TESTE;
    ELSEIF EMP_SALARY < SALARY_THR THEN
        SELECT CONCAT('O funcionário ', EMP_ID, ' ganha menos que $', SALARY_THR) AS TESTE;
    ELSE
        SELECT CONCAT('O funcionário ', EMP_ID, ' ganha exatamente $', SALARY_THR) AS TESTE;
    END IF;
END //
DELIMITER ;

CALL proc_compare_salary(10001, 100000);

