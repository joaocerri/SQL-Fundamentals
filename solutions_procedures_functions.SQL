CREATE PROCEDURE get_average_per_title(IN P_DEPT_NO CHAR(4), IN P_TITLE VARCHAR(50), OUT P_AVG_SALARY DECIMAL(10,2))
BEGIN 
    SELECT AVG(S.SALARY) INTO P_AVG_SALARY FROM SALARIES S
    JOIN TITLES T ON S.EMP_NO = T.EMP_NO 
    JOIN DEPT_EMP DE ON S.EMP_NO = DE.EMP_NO
    WHERE DE.DEPT_NO = P_DEPT_NO AND T.TITLE = P_TITLE 
    AND S.TO_DATE = '9999-01-01' AND T.TO_DATE = '9999-01-01' AND DE.TO_DATE = '9999-01-01';
END //
DELIMITER ;

