CREATE PROCEDURE proc_get_func_sal(IN e_emp_no INT)
BEGIN 
    SELECT s.salary, e.emp_no FROM employees e 
    JOIN salaries s ON s.emp_no = e.emp_no
    WHERE s.to_date = '9999-01-01' AND e.emp_no = e_emp_no;
END //
DELIMITER ;

