CREATE FUNCTION emp_is_manager(EMP_ID INT)
RETURNS CHAR(3)
DETERMINISTIC 
BEGIN 
    DECLARE IS_MANAGER CHAR(3);
    DECLARE EXIST INT;
    
    SELECT EMP_NO INTO EXIST FROM DEPT_MANAGER
    WHERE TO_DATE = '9999-01-01' AND EMP_NO = EMP_ID
    LIMIT 1;
    
    IF EXIST IS NULL THEN 
        SET IS_MANAGER = 'Não';
    ELSE
        SET IS_MANAGER = 'Sim';
    END IF;
    
    RETURN IS_MANAGER;
END //
DELIMITER ; 

