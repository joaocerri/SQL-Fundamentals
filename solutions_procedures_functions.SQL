CREATE FUNCTION get_func_sal(e_emp_no INT)
RETURNS DECIMAL(10,2)
DETERMINISTIC 
BEGIN 
    DECLARE v_salary DECIMAL(10,2);
    SELECT salary INTO v_salary FROM salaries
    WHERE emp_no = e_emp_no AND to_date = '9999-01-01';
    RETURN v_salary;
END //
