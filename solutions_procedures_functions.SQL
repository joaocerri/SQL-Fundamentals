CREATE PROCEDURE update_salary_by_title(IN P_TITLE VARCHAR(50), IN P_DEPT_NO CHAR(4), IN P_PERCENTAGE DECIMAL(5,2))
BEGIN
    UPDATE SALARIES S
    JOIN TITLES T ON S.EMP_NO = T.EMP_NO 
    JOIN DEPT_EMP DE ON S.EMP_NO = DE.EMP_NO
    SET S.SALARY = S.SALARY * (1 + (P_PERCENTAGE / 100))
    WHERE T.TITLE = P_TITLE AND DE.DEPT_NO = P_DEPT_NO 
    AND S.TO_DATE = '9999-01-01' AND T.TO_DATE = '9999-01-01' AND DE.TO_DATE = '9999-01-01';
END //
DELIMITER ;

