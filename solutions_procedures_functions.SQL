CREATE PROCEDURE get_dept_relatory(IN P_DEPT_NO CHAR(4), OUT P_TOTAL_EMP INT, OUT P_AVG_SALARY DECIMAL(10,2), OUT P_MAX_SALARY INT)
BEGIN	
    SELECT COUNT(DE.EMP_NO), ROUND(AVG(S.SALARY), 2), MAX(S.SALARY) INTO P_TOTAL_EMP, P_AVG_SALARY, P_MAX_SALARY
    FROM DEPT_EMP DE
    JOIN SALARIES S ON DE.EMP_NO = S.EMP_NO
    WHERE DE.DEPT_NO = P_DEPT_NO AND DE.TO_DATE = '9999-01-01' AND S.TO_DATE = '9999-01-01';
END //
DELIMITER ;

