CREATE PROCEDURE get_func_per_dept(P_DEPT_NO CHAR(4))
BEGIN
    SELECT DE.EMP_NO, CONCAT(E.FIRST_NAME, " ", E.LAST_NAME) AS NOME, E.HIRE_DATE FROM EMPLOYEES E
    JOIN DEPT_EMP DE ON E.EMP_NO = DE.EMP_NO
    WHERE DE.TO_DATE = '9999-01-01' AND DE.DEPT_NO = P_DEPT_NO
    ORDER BY E.LAST_NAME, E.FIRST_NAME;
