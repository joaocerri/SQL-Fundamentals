CREATE FUNCTION rank_emp_per_salary(EMP_ID INT)
RETURNS VARCHAR(10)
DETERMINISTIC 
BEGIN 
    DECLARE POSITION VARCHAR(10);
    DECLARE SALARIO INT;
    SET SALARIO = GET_SALARY(EMP_ID);
    
    SET POSITION = CASE 
        WHEN SALARIO < 60000 THEN 'Baixo'
        WHEN SALARIO BETWEEN 60001 AND 80000 THEN 'Moderado'
        ELSE 'Alto'
    END;
    RETURN POSITION;
END //
